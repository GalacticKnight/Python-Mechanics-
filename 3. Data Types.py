#String
x = str("Hello World")
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#Integer
x = int(20)
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#Float
x = float(20.5)
#display x:
print(x)
#display the data type of x:
print(type(x)) 


#Complex
x = complex(1j)
#display x:
print(x)
#display the data type of x:
print(type(x)) 


#List
x = list(("apple", "banana", "cherry"))
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#Tuple
x = tuple(("apple", "banana", "cherry"))
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#Range
x = range(6)
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#dictionary
x = dict(name="John", age=36)
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#set
x = set(("apple", "banana", "cherry"))
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#Frozenset
x = frozenset(("apple", "banana", "cherry"))
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#Bool
x = bool(5)
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#Byte
x = b"Hello"
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#bytearray
x = bytearray(5)
#display x:
print(x)
#display the data type of x:
print(type(x)) 

#memoryview
x = memoryview(bytes(5))
#display x:
print(x)
#display the data type of x:
print(type(x)) 


